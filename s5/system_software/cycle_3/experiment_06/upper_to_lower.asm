ASSUME CS:CODE, DS:DATA
DATA SEGMENT
  str DB 'hElLo wOrLD$'
DATA ENDS
CODE SEGMENT
  START:
    MOV AX, DATA
    MOV DS, AX
    XOR AX, AX
    LEA SI, str

  CONVERT:
    MOV AL, [SI]
    CMP AL, '$'
    JE DISPLAY
    CMP AL,5AH
    JB TO_LOWER
  
  TO_LOWER:
    ADD AL, 20H
    MOV [SI], AL
    INC SI
    JMP CONVERT

  DISPLAY:
    LEA DX, str
    MOV AH, 09H
    INT 21H

  CLEANUP:
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START

